function handleButtonClick(action) {
    const actions = action.split('_');
    
    switch(actions[0]) {
        case 'welcome':
            showWelcomeMessage();
            break;
        case 'modules':
            showModules();
            break;
        case 'lecons':
            showLessons(parseInt(actions[1]));
            break;
        case 'lesson':
            showFullLesson(parseInt(actions[1]));
            break;
        case 'next':
            if (actions[1] === 'lesson') {
                showNextLesson(parseInt(actions[2]));
            }
            break;
        case 'quiz':
            if (actions[1] === 'lesson') {
                startLessonQuiz(parseInt(actions[2]));
            } else if (actions[1] === 'module') {
                startQuiz(parseInt(actions[2]));
            } else {
                showQuizMenu();
            }
            break;
        case 'glossaire':
            showGlossaire();
            break;
        case 'ressources':
            showRessources();
            break;
        case 'aide':
            showHelp();
            break;
        default:
            processUserMessage(action);
            break;
    }
}

